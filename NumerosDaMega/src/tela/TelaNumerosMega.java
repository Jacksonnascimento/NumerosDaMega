    public void preencher(int qtVezes){
        zerarList();
        String numerosText = "";
        Numeros numeros = new Numeros();
        for(int i = 1; i <= qtVezes; i++){  
            for(Object nume : numeros.getNumeros()){
               
                if((int) nume < 10){
                    numerosText += "0" + nume + "            ";
                } else {
                    numerosText += nume + "            ";
                }
            }
            itens.addElement(numerosText);
            numerosText = "";
        }
      listaApostas.setModel(itens);
    }
    
    public void eventoApostas(){
        if(numeroApostas.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe uma quantidade de apostas");
        } else {
            int qtAposta = Integer.parseInt(numeroApostas.getText());
            if (qtAposta > 0) {
                preencher(qtAposta);
            } else {
                JOptionPane.showMessageDialog(null, "Digite uma quantidade de apostas superior a 0");
            }
        }
        
        
     
        
        
    }

